// ─────────────────────────────────────────────
//  IngredientKnowledgeBase
//  Maps normalised INCI names → functions,
//  irritancy level, and allergen tags.
//  Used to enrich raw OBF ingredient data.
// ─────────────────────────────────────────────

struct IngredientInfo {
    let commonName:  String?
    let functions:   [IngredientFunction]
    let description: String
    let irritancy:   IrritancyLevel
    let isAllergen:  Bool
    let allergenTags: [String]
    let riskProfile: IngredientRiskProfile?  // nil = no documented chemical hazard

    // Backward-compatible: riskProfile defaults to nil so all existing entries compile.
    init(commonName: String?,
         functions: [IngredientFunction],
         description: String,
         irritancy: IrritancyLevel,
         isAllergen: Bool,
         allergenTags: [String],
         riskProfile: IngredientRiskProfile? = nil) {
        self.commonName  = commonName
        self.functions   = functions
        self.description = description
        self.irritancy   = irritancy
        self.isAllergen  = isAllergen
        self.allergenTags = allergenTags
        self.riskProfile = riskProfile
    }
}

enum IngredientKnowledgeBase {

    static func lookup(_ normalized: String) -> IngredientInfo? {
        if let hit = db[normalized] { return hit }
        // Partial key match for compound names (e.g. "sodium hyaluronate crosspolymer")
        for (key, info) in db where normalized.contains(key) { return info }
        return nil
    }

